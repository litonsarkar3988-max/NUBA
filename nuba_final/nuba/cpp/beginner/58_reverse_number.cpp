// Beginner C++ Exercise 58: Reverse digits of number

#include <iostream>
#include <string>
#include <cmath>
using namespace std;

int main() {
    // Reverse digits of number
    cout << "Exercise: Reverse digits of number" << endl;
    
    // Implementation
    for (int i = 1; i <= 10; i++) {
        cout << i << " ";
    }
    cout << endl;
    
    return 0;
}
