// Beginner: Arithmetic Operations
#include <iostream>
#include <cmath>
using namespace std;

int main() {
    int a = 15, b = 4;
    cout << "a = " << a << ", b = " << b << endl;
    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << " (integer division)" << endl;
    cout << "a % b = " << a % b << endl;
    cout << "a ^ b = " << pow(a, b) << endl;
    double x = 15.0, y = 4.0;
    cout << "15.0 / 4.0 = " << x / y << endl;
    return 0;
}
