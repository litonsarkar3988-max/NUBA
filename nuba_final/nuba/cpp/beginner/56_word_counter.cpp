// Beginner C++ Exercise 56: Word counter in string

#include <iostream>
#include <string>
#include <cmath>
using namespace std;

int main() {
    // Word counter in string
    cout << "Exercise: Word counter in string" << endl;
    
    // Implementation
    for (int i = 1; i <= 10; i++) {
        cout << i << " ";
    }
    cout << endl;
    
    return 0;
}
