// Beginner C++: cmath functions

#include <iostream>
#include <cmath>
using namespace std;
int main() {
    cout << "abs(-7) = " << abs(-7) << endl;
    cout << "pow(2,8) = " << pow(2,8) << endl;
    cout << "sqrt(144) = " << sqrt(144) << endl;
    cout << "ceil(3.2) = " << ceil(3.2) << endl;
    cout << "floor(3.9) = " << floor(3.9) << endl;
    cout << "round(3.5) = " << round(3.5) << endl;
    cout << "log(M_E) = " << log(M_E) << endl;
    cout << "sin(0) = " << sin(0) << endl;
    cout << "cos(0) = " << cos(0) << endl;
    return 0;
}
